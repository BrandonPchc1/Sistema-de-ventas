    private void borrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarBtnActionPerformed
        int filaSeleccionada = tablaProductos.getSelectedRow();
        try {
            if (filaSeleccionada >= 0){ //borrar por fila seleccionada
                if (JOptionPane.showConfirmDialog(null,
                        "¿Quieres eliminar este producto?",
                        "Confirmar",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                    productCrud.borrarProducto(modeloTabla.getValueAt(filaSeleccionada, 0).toString());
                    cargarDatosTabla();
                    calcularTotal(); //vuelve a calcular el total
                }
            }else{
                JOptionPane.showMessageDialog(null, "Selecciona una fila", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error general", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_borrarBtnActionPerformed

