    private void reporteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reporteBtnActionPerformed
        try {
            if (JOptionPane.showConfirmDialog(null,
                        "¿Quieres crear un nuevo reporte de estas ventas?",
                        "Confirmar",
                        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String nomDoc = JOptionPane.showInputDialog(null,
                        "Ingrese el nombre del documento",
                        "Nombre del documento",
                        JOptionPane.QUESTION_MESSAGE);
                if (nomDoc.equals("")){nomDoc = "Reporte_";}
                    generarReporte.crearReporte(modeloTabla, nomDoc+"_", sesionUsuario.getInstance().getNombre());
                }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error general", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_reporteBtnActionPerformed
    private void calcularTotal(){ //calcula el total y lo pone en el texto
        try {
            double total_fin = 0;
            int filas = modeloTabla.getRowCount();
            if (filas >= 1){ 
                for (int i=0;i<filas;i++){
                total_fin += Double.parseDouble(modeloTabla.getValueAt(i, 4).toString());
                }
            }
            totalCalculado.setText(String.valueOf(total_fin));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al calcular el total", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
