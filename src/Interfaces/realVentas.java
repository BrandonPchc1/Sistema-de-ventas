    private boolean cargarDatosTabla(DefaultTableModel modelo){
        try {
            modelo.setRowCount(0);
            double total = Integer.parseInt(cantField.getText().strip()) * Double.parseDouble(precioVentaField.getText().strip());
            Object[] fila = {
                nProdField.getText().strip(),
                DescField.getText(),
                cantField.getText().strip(),
                precioVentaField.getText().strip(),
                total
               };
           modelo.addRow(fila);
           return true; //Si se realizo correctamente retornara true
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al ingresar los Datos", "Error en datos", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }    
