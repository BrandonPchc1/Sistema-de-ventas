    private boolean verificarEspacios(){//retorna false si no hay datos
        if (nProdField.getText().trim().isEmpty() || precioVentaField.getText().strip().isEmpty() || cantField.getText().strip().isEmpty()){
                JOptionPane.showMessageDialog(null, "Debes ingresar todos los datos", "Ingresa los datos", JOptionPane.ERROR_MESSAGE);
                return false;
        }
        return true;
    }
