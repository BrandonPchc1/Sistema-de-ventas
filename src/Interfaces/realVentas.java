    private void cargarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarBtnActionPerformed
        if (!verificarEspacios()){ //Si no se han colocado todos los elementos necesarios se retornara
                aggBtn.setEnabled(false);
                return;
            }
        if (cargarDatosTabla(modeloTabla)){//Si la tabla no se carga correctamente el boton de agregar no se desbloquea
            aggBtn.setEnabled(true);
        }else{
            aggBtn.setEnabled(false);
        }
    }//GEN-LAST:event_cargarBtnActionPerformed

    private void aggBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aggBtnActionPerformed
        try {//Boton que agrega los elementos de la tabla
            if (!verificarEspacios()){
                return;
            }
            nombreProd =nProdField.getText().strip();
            descripcion = DescField.getText();
            cantidad = Integer.parseInt(cantField.getText().strip());
            precio = Double.parseDouble(precioVentaField.getText().strip());
            total = precio * cantidad;
            productCrud.crearOActualizarProducto(nombreProd, descripcion, cantidad, precio,total);
            borrarBtnActionPerformed(evt);
            aggBtn.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error general", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_aggBtnActionPerformed
